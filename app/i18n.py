def detect_os_language() -> str:
    """Resolve the Windows display language to a supported UI locale.

    Used when the user has never explicitly chosen a language
    (cfg["ui_language"] is empty — the shipped seed no longer carries one, see
    config.SEED_WHITELIST). Chinese picks Traditional for TW/HK/MO locales and
    Simplified otherwise; every unknown language falls back to English."""
    try:
        import ctypes
        import locale as _locale
        lcid = ctypes.windll.kernel32.GetUserDefaultUILanguage()
        name = (_locale.windows_locale.get(lcid) or "").replace("-", "_")
    except Exception:
        name = ""
    if not name:
        return "en"
    base = name.split("_")[0].lower()
    if base == "zh":
        region = (name.split("_")[1] if "_" in name else "").upper()
        return "zh-Hant" if region in ("TW", "HK", "MO", "HANT") else "zh"
    if base in STRINGS:
        return base
    return "en"


def resolve_language(configured: str) -> str:
    """The language the app should run in: the user's explicit choice when one
    is stored and known, else the OS display language."""
    if configured and configured in STRINGS:
        return configured
    return detect_os_language()


def set_language(lang: str):
    global _current
    _current = lang if lang in STRINGS else "en"


def t(key: str, **kw) -> str:
    s = STRINGS.get(_current, STRINGS["en"]).get(key) or STRINGS["en"].get(key, key)
    return s.format(**kw) if kw else s
